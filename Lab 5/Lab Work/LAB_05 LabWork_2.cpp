#include <iostream>
using namespace std;

int main()
{
    int i = 1; // Initial value

    // do-while loop: executes block at least once
    do
    {
        cout << i << endl; // Output value
        i++; // Increment counter
    }
    while(i <= 10); // Condition check after execution

    return 0; // Success
}

